package main

import (
	"flag"
	"log"
	"os"

	"github.com/pkg/profile"

	"github.com/hajimehoshi/go2dotnet/gowasm2csharp"
)

var (
	flagWasm      = flag.String("wasm", "", "WebAssembly file generated by Go")
	flagNamespace = flag.String("namespace", "", "Namespace")
	flagProfile   = flag.Bool("profile", false, "Take profiles")
)

func main() {
	flag.Parse()
	if *flagProfile {
		defer profile.Start().Stop()
	}

	outDir := "autogen"
	if err := os.RemoveAll(outDir); err != nil {
		log.Fatal(err)
	}
	if err := os.MkdirAll(outDir, 0755); err != nil {
		log.Fatal(err)
	}

	if err := gowasm2csharp.Generate(outDir, *flagWasm, *flagNamespace); err != nil {
		log.Fatal(err)
	}
}
